package com.java.class02;

public class AritmaticOperation {

	public static void main(String[] args) {
		int a=10 ,b= 20;
				
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);   // 10/ 20 = 0.5 due to int so 0
		System.out.println(a%b);
		System.out.println();
		
		System.out.println(30 /10);  // remove the last digit from last number
		System.out.println(25 /10);
		System.out.println(525 /10);
		System.out.println(48 /10);
		
	}

}
